package model;

public class Usuario {
	private int codigo;
	private String nombre, apellido,puesto,usuario,clave,facceso,abre_ofic;
	
	public int getCodigo() {
		return codigo;
	}
	public void setCodigo(int codigo) {
		this.codigo = codigo;
	}
	public String getNombre() {
		return nombre;
	}
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	public String getApellido() {
		return apellido;
	}
	public void setApellido(String apellido) {
		this.apellido = apellido;
	}
	public String getPuesto() {
		return puesto;
	}
	public void setPuesto(String puesto) {
		this.puesto = puesto;
	}
	public String getUsuario() {
		return usuario;
	}
	public void setUsuario(String usuario) {
		this.usuario = usuario;
	}
	public String getClave() {
		return clave;
	}
	public void setClave(String clave) {
		this.clave = clave;
	}
	public String getFacceso() {
		return facceso;
	}
	public void setFacceso(String facceso) {
		this.facceso = facceso;
	}
	public String getAbre_ofic() {
		return abre_ofic;
	}
	public void setAbre_ofic(String abre_ofic) {
		this.abre_ofic = abre_ofic;
	}
	
	
}
