package model;

public class Tramite {
	private int cod_exp;
	private String cod_tram;
	private String fecha_registro_tram;
	private String abrev_ofic;
	private String tipo_doc;
	
	public int getCod_exp() {
		return cod_exp;
	}
	public void setCod_exp(int cod_exp) {
		this.cod_exp = cod_exp;
	}
	public String getCod_tram() {
		return cod_tram;
	}
	public void setCod_tram(String cod_tram) {
		this.cod_tram = cod_tram;
	}
	public String getFecha_registro_tram() {
		return fecha_registro_tram;
	}
	public void setFecha_registro_tram(String fecha_registro_tram) {
		this.fecha_registro_tram = fecha_registro_tram;
	}
	public String getAbrev_ofic() {
		return abrev_ofic;
	}
	public void setAbrev_ofic(String abrev_ofic) {
		this.abrev_ofic = abrev_ofic;
	}
	public String getTipo_doc() {
		return tipo_doc;
	}
	public void setTipo_doc(String tipo_doc) {
		this.tipo_doc = tipo_doc;
	}
	
	
}
